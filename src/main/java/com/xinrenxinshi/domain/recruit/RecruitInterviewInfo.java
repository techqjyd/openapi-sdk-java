/**
 * <面试轮次>.
 *
 * @author peng cheng
 * @date 2019/10/17
 * @since
 */
public class RecruitInterviewInfo {

    /**
     * 面试轮次
     */
    private String interviewRounds;
    /**
     * 面试日期
     */
    private String interviewDate;
    /**
     * 面试时间
     */
    private String interviewTime;
    /**
     * 面试负责人
     */
    private String interviewAccount;
    /**
     * 面试职位
     */
    private String interviewJob;
    /**
     * 面试官
     */
    private String interviewEmployee;
    /**
     * 面试邀约状态
     */
    private String interviewInviteStatus;
    /**
     * 面试签到状态
     */
    private String interviewSignStatus;
    /**
     * 面试结果
     */
    private String interviewFeedBack;
    /**
     * 面试反馈时间
     */
    private String interviewFeedBackTime;

    public String getInterviewRounds() {
        return interviewRounds;
    }

    public void setInterviewRounds(String interviewRounds) {
        this.interviewRounds = interviewRounds;
    }

    public String getInterviewDate() {
        return interviewDate;
    }

    public void setInterviewDate(String interviewDate) {
        this.interviewDate = interviewDate;
    }

    public String getInterviewTime() {
        return interviewTime;
    }

    public void setInterviewTime(String interviewTime) {
        this.interviewTime = interviewTime;
    }

    public String getInterviewAccount() {
        return interviewAccount;
    }

    public void setInterviewAccount(String interviewAccount) {
        this.interviewAccount = interviewAccount;
    }

    public String getInterviewJob() {
        return interviewJob;
    }

    public void setInterviewJob(String interviewJob) {
        this.interviewJob = interviewJob;
    }

    public String getInterviewEmployee() {
        return interviewEmployee;
    }

    public void setInterviewEmployee(String interviewEmployee) {
        this.interviewEmployee = interviewEmployee;
    }

    public String getInterviewInviteStatus() {
        return interviewInviteStatus;
    }

    public void setInterviewInviteStatus(String interviewInviteStatus) {
        this.interviewInviteStatus = interviewInviteStatus;
    }

    public String getInterviewSignStatus() {
        return interviewSignStatus;
    }

    public void setInterviewSignStatus(String interviewSignStatus) {
        this.interviewSignStatus = interviewSignStatus;
    }

    public String getInterviewFeedBack() {
        return interviewFeedBack;
    }

    public void setInterviewFeedBack(String interviewFeedBack) {
        this.interviewFeedBack = interviewFeedBack;
    }

    public String getInterviewFeedBackTime() {
        return interviewFeedBackTime;
    }

    public void setInterviewFeedBackTime(String interviewFeedBackTime) {
        this.interviewFeedBackTime = interviewFeedBackTime;
    }
}
