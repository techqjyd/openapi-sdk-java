package com.xinrenxinshi.domain.recruit;

public class RecruitmentResumeProjectExperienceModel {

    // 开始时间
    private String startDate;
    // 结束时间
    private String endDate;
    // 项目名称
    private String projectName;
    // 项目描述
    private String projectDetail;
    // 个人职责
    private String liability;
    // 所属公司
    private String company;
    // 项目链接
    private String projectLink;
    // 项目成绩
    private String achievements;
    // 项目使用技术
    private String technology;
    // 角色
    private String role;
    // 项目人数
    private String projectMemberCount;

    public String getStartDate() {
        return startDate;
    }

    public void setStartDate(String startDate) {
        this.startDate = startDate;
    }

    public String getEndDate() {
        return endDate;
    }

    public void setEndDate(String endDate) {
        this.endDate = endDate;
    }

    public String getProjectName() {
        return projectName;
    }

    public void setProjectName(String projectName) {
        this.projectName = projectName;
    }

    public String getProjectDetail() {
        return projectDetail;
    }

    public void setProjectDetail(String projectDetail) {
        this.projectDetail = projectDetail;
    }

    public String getLiability() {
        return liability;
    }

    public void setLiability(String liability) {
        this.liability = liability;
    }

    public String getCompany() {
        return company;
    }

    public void setCompany(String company) {
        this.company = company;
    }

    public String getProjectLink() {
        return projectLink;
    }

    public void setProjectLink(String projectLink) {
        this.projectLink = projectLink;
    }

    public String getAchievements() {
        return achievements;
    }

    public void setAchievements(String achievements) {
        this.achievements = achievements;
    }

    public String getTechnology() {
        return technology;
    }

    public void setTechnology(String technology) {
        this.technology = technology;
    }

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }

    public String getProjectMemberCount() {
        return projectMemberCount;
    }

    public void setProjectMemberCount(String projectMemberCount) {
        this.projectMemberCount = projectMemberCount;
    }
}
